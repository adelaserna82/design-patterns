package Builder;

public class Manual {
    private String name;
    private String engine;
    private String seats;
    private String tripComputer;
    private String gps;

    public Manual() {
    }

    public String getName() {
        return name;
    }

    public String getEngine() {
        return engine;
    }

    public String getSeats() {
        return seats;
    }

    public String getTripComputer() {
        return tripComputer;
    }

    public String getGps() {
        return gps;
    }
}
